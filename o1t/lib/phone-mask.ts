/**
 * OpenAI 등 외부 전송 전 전화번호 마스킹 (개인정보 보호).
 * 한국 휴대폰/일반 번호 패턴을 찾아 중간 자리를 * 로 치환합니다.
 */

/**
 * 텍스트 내 전화번호를 마스킹합니다.
 * 예: 010-1234-5678 → 010-****-5678, 01012345678 → 010****5678
 */
export function maskPhoneNumbers(text: string): string {
  let out = text;
  // 010-1234-5678, 010-123-4567, 010 1234 5678
  out = out.replace(/\b(010)([-.\s]?)(\d{3,4})([-.\s]?)(\d{4})\b/g, (_, p1, p2, _mid, p4, p5) => `${p1}${p2}****${p4}${p5}`);
  // 01X-XXX-XXXX, 02-123-4567 등
  out = out.replace(/\b(0\d{1,2})([-.\s]?)(\d{3,4})([-.\s]?)(\d{4})\b/g, (_, p1, p2, _mid, p4, p5) => `${p1}${p2}****${p4}${p5}`);
  // 숫자만 11자리 01012345678
  out = out.replace(/\b(010)(\d{4})(\d{4})\b/g, "$1****$3");
  return out;
}
